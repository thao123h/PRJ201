package model;

/**
 *
 * @author asus
 */
public class OrderDetail {
    int id;
    Order order;
    Product product;
    int num;
    int totalMoney;

    public OrderDetail() {
    }

    public OrderDetail(int id, Order order, Product product, int num, int totalMoney) {
        this.id = id;
        this.order = order;
        this.product = product;
        this.num = num;
        this.totalMoney = totalMoney;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Order getOrder() {
        return order;
    }

    public void setOrder(Order order) {
        this.order = order;
    }

    public Product getProduct() {
        return product;
    }

    public void setProduct(Product product) {
        this.product = product;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(int totalMoney) {
        this.totalMoney = totalMoney;
    }
    
    
    
}
