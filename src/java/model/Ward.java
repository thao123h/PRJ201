package model;

/**
 *
 * @author asus
 */
public class Ward {
     int id;
      String name;
      District district;

    public Ward() {
    }

    public Ward(int id, String name, District district) {
        this.id = id;
        this.name = name;
        this.district = district;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public District getDistrict() {
        return district;
    }

    public void setDistrict(District district) {
        this.district = district;
    }
      
}
