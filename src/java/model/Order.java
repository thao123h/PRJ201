package model;

/**
 *
 * @author asus
 */
public class Order {
    int id;
    User user ;
    int status;
int totalMoney;

    public Order() {
    }

    public Order(int id, User user, int status, int totalMoney) {
        this.id = id;
        this.user = user;
        this.status = status;
        this.totalMoney = totalMoney;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(int totalMoney) {
        this.totalMoney = totalMoney;
    }

   
   
    
    
}
